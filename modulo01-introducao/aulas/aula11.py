#Incremento e Decremento

#Soma
contadorSoma = 3
contadorSoma += 1
print('Soma = ', contadorSoma)

#Subtração
contadorSubtracao = 3
contadorSubtracao -= 1
print('Subtração = ', contadorSubtracao)

#Multiplicação
contadorMultiplicacao = 3
contadorMultiplicacao *= 2
print('Multiplicação = ', contadorMultiplicacao)

#Divisão
contadorDivisao = 4
contadorDivisao /= 2
print('Divisão = ', contadorDivisao)